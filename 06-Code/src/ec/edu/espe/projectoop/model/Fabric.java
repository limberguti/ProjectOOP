public class Fabric {
    private int  number;
    private String color;
    private String typeOfFabric;
    private String fabricMeters;
    private String docmuent;

    
    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getTypeOfFabric() {
        return typeOfFabric;
    }

    public void setTypeOfFabric(String typeOfFabric) {
        this.typeOfFabric = typeOfFabric;
    }

    public String getFabricMeters() {
        return fabricMeters;
    }

    public void setFabricMeters(String fabricMeters) {
        this.fabricMeters = fabricMeters;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int numero) {
        this.number = numero;
    }

    public String getDocmuent() {
        return docmuent;
    }

    public void setDocmuent(String docmuent) {
        this.docmuent = docmuent;
    }

    
}